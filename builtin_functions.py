# Program to demonstrate built-in functions

lst = [5, 2, 9, 1]

# List functions
print("Length:", len(lst))
print("Max:", max(lst))
print("Min:", min(lst))
print("Sorted:", sorted(lst))

# String functions
text = "python"
print("Upper:", text.upper())
print("Lower:", text.lower())

# Dictionary functions
d = {"a": 1, "b": 2}
print("Keys:", d.keys())
print("Values:", d.values())
