# Program to iterate through different data types

# String
text = "Python"
print("String iteration:")
for ch in text:
    print(ch)

# List
lst = [1, 2, 3, 4]
print("\nList iteration:")
for item in lst:
    print(item)

# Dictionary
d = {"a": 1, "b": 2, "c": 3}
print("\nDictionary iteration:")
for key, value in d.items():
    print(key, ":", value)
