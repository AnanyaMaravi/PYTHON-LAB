# Program to demonstrate int, float and type conversion

# Integer value
a = 10
print("Integer value:", a)
print("Type of a:", type(a))

# Float value
b = 5.5
print("Float value:", b)
print("Type of b:", type(b))

# Type conversion: int to float
c = float(a)
print("After converting int to float:", c)
print("Type of c:", type(c))

# Type conversion: float to int
d = int(b)
print("After converting float to int:", d)
print("Type of d:", type(d))
